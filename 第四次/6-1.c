#include <stdio.h>

int main() {
    int a[5] = {10, 20, 30, 40, 50};
    
    // 1. 打印地址值（数值相同，类型不同）
    printf("a（首元素地址）: %p\n", a);       // 输出数组首元素地址（如 0x7ffeefbff5a0）
    printf("&a（整个数组地址）: %p\n", &a);   // 输出与上一行相同的数值，但语义是“数组整体的地址”
    
    // 2. 打印指针类型对应的大小（体现类型差异）
    printf("\nsizeof(a): %zu\n", sizeof(a));   // 数组总大小：5*4=20（32位/64位int均为4字节）
    printf("sizeof(&a): %zu\n", sizeof(&a));   // 指针大小：8字节（64位系统）或4字节（32位系统）
    printf("sizeof(a[0]): %zu\n", sizeof(a[0])); // 单个元素大小：4字节
    
    // 3. 指针运算差异（关键区别！）
    printf("\na + 1（首元素指针+1）: %p\n", a + 1);     // 跳过1个int（+4字节），指向a[1]
    printf("&a + 1（数组指针+1）: %p\n", &a + 1);   // 跳过整个数组（+20字节），指向数组末尾后一位
    
    // 4. 解引用差异
    printf("\n*a（首元素指针解引用）: %d\n", *a);       // 等价于a[0]，输出10
    printf("(*&a)[0]（数组指针解引用）: %d\n", (*&a)[0]); // 解引用&a得到整个数组，再取第0个元素，输出10
    system("pause");
    return 0;
}